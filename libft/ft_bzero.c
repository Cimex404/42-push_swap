#include <stdlib.h>
#include "libft.h"

void	*ft_bzero(void *dst, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *) dst)[i] = 0;
		i ++;
	}
	return (dst);
}
